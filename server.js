const card = document.querySelector('.card');

let usedQuestions = [];
let currentQuestionIndex = 0;

// تهيئة العداد
counterEl.textContent = `1/${questions.length}`;

function getRandomQuestion() {
  if (usedQuestions.length === questions.length) {
    usedQuestions = [];
    currentQuestionIndex = 0;
  }
  
  let availableQuestions = questions.filter((q, index) => !usedQuestions.includes(index));
  if (availableQuestions.length === 0) {
    availableQuestions = [...questions];
    usedQuestions = [];
    currentQuestionIndex = 0;
  }
  
  const randomIndex = Math.floor(Math.random() * availableQuestions.length);
  const selectedQuestionIndex = questions.indexOf(availableQuestions[randomIndex]);
  
  usedQuestions.push(selectedQuestionIndex);
  currentQuestionIndex = selectedQuestionIndex + 1;
  return availableQuestions[randomIndex];
}

function showNextQuestion() {
  card.classList.remove('animate-card');
  void card.offsetWidth; // Trigger reflow
  card.classList.add('animate-card');
  
  const {question, note} = getRandomQuestion();
  questionEl.textContent = question;
  noteEl.textContent = note;
  
  // تحديث العداد
  counterEl.textContent = `${currentQuestionIndex}/${questions.length}`;
}

function shareQuestion() {
  const currentQuestion = questionEl.textContent;
  const currentNote = noteEl.textContent;
  const shareText = `سؤال اليوم:\n${currentQuestion}\n\n${currentNote}\n\nجرب تطبيق "افتح توبيك" لأسئلة عميقة تقوي العلاقات`;
  
  if (navigator.share) {
    navigator.share({
      title: 'افتح توبيك',
      text: shareText,
      url: window.location.href
    }).catch(err => {
      console.log('Error sharing:', err);
      copyToClipboard(shareText);
    });
  } else {
    copyToClipboard(shareText);
  }
}

function copyToClipboard(text) {
  navigator.clipboard.writeText(text).then(() => {
    alert('تم نسخ السؤال إلى الحافظة، يمكنك مشاركته الآن');
  }).catch(err => {
    console.error('Failed to copy:', err);
    // Fallback for older browsers
    const textarea = document.createElement('textarea');
    textarea.value = text;
    document.body.appendChild(textarea);
    textarea.select();
    document.execCommand('copy');
    document.body.removeChild(textarea);
    alert('تم نسخ السؤال إلى الحافظة، يمكنك مشاركته الآن');
  });
}

